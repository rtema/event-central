#!/usr/bin/env python3
"""Deep ZUGFeRD / XRechnung validation for the Event Central invoice fuzzer.

``generate_invoices.py`` already does *shallow* document checks: it confirms the
returned ``invoicePdf`` / ``invoiceXml`` are valid base64, that the PDF starts
with ``%PDF`` and that the XML is well-formed. That catches gross breakage but
says nothing about whether the documents are *actually* conformant e-invoices.

This module adds the deep check by calling two dockerized validators over HTTP
(see ``docker-compose.yml``):

  * Mustang (:8088) validates the WHOLE ZUGFeRD/Factur-X PDF -- PDF/A-3
    conformance + the embedded XML against XML-Schema and the EN16931 / profile
    Schematron. It can also extract the embedded XML from a PDF.
  * KoSIT (:8089), the official German B2G reference validator, validates
    XRechnung / CII XML against EN16931 and the XRechnung CIUS (BR-DE) rules.

How the two functions use them:

  * ``validate_xrechnung(xml, report)`` -> KoSIT (authoritative for the XML).
  * ``validate_zugferd(pdf, report)``   -> Mustang on the whole PDF (primary);
    then the embedded XML is extracted and also run through KoSIT as a secondary
    second opinion (folded in as warnings; a plain ZUGFeRD CII that is not an
    XRechnung matches no scenario and is treated as a notice, not a failure).

Findings are folded into the fuzzer's existing ``Report`` so a non-conformant
document generated by the API shows up as a normal failure.

How to use it
-------------
1. In ``generate_invoices.py``, import this module once, then call the two
   functions where the 201 response is checked (inside ``_validate_success``),
   passing the response fields and the ``Report``::

       from src import validate_invoices        # or: import validate_invoices

       # ... in _validate_success(data, resp, report): ...
       validate_invoices.validate_zugferd(data.get("invoicePdf"), report)
       validate_invoices.validate_xrechnung(data.get("invoiceXml"), report)

   Each runs the cheap structural checks then the deep HTTP validation and folds
   any findings into ``report`` (and returns a ``ValidationOutcome``). They fully
   replace the module's original ``_validate_pdf`` / ``_validate_xml`` calls.

2. Standalone, over already-saved outputs::

       python -m src.validate_invoices invoices/            # a directory
       python -m src.validate_invoices some-invoice.pdf ...  # explicit files

Configuration (environment variables)
    INVOICE_VALIDATOR_MUSTANG_URL       Mustang base URL, default http://localhost:8088
    INVOICE_VALIDATOR_KOSIT_URL           KoSIT base URL,   default http://localhost:8089
    INVOICE_VALIDATOR_REQUIRED  "1" -> a validator that is down/unreachable is a
                                hard error; default "0" -> degrade to a warning
                                and keep the fuzzer running. (Secondary KoSIT on
                                the embedded XML never hard-fails.)
    INVOICE_VALIDATOR_NOTICES   "1" -> surface NOTICE-level findings too.
    INVOICE_VALIDATOR_TIMEOUT   per-request HTTP timeout in seconds (default 150).
"""

