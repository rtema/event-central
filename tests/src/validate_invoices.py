def _iter_targets(paths: list[str]) -> list[str]:
    import glob
    out: list[str] = []
    for p in paths:
        if os.path.isdir(p):
            for ext in ("*.pdf", "*.xml"):
                out.extend(sorted(glob.glob(os.path.join(p, "**", ext),
                                            recursive=True)))
        else:
            out.append(p)
    return out


def _main(argv: list[str]) -> int:
    import argparse
    parser = argparse.ArgumentParser(
        description="Validate ZUGFeRD/Factur-X PDFs (Mustang, whole PDF + KoSIT "
                    "on the embedded XML) or XRechnung/CII XML (KoSIT).")
    parser.add_argument("paths", nargs="+",
                        help="files and/or directories to validate")
    parser.add_argument("--mustang-url", default=MUSTANG_URL,
                        help=f"Mustang base URL (default {MUSTANG_URL})")
    parser.add_argument("--kosit-url", default=KOSIT_URL,
                        help=f"KoSIT base URL (default {KOSIT_URL})")
    parser.add_argument("--notices", action="store_true",
                        help="include NOTICE-level findings")
    parser.add_argument("-v", "--verbose", action="store_true",
                        help="print every finding, not just a summary line")
    args = parser.parse_args(argv)

    global INCLUDE_NOTICES, _default_client, _default_kosit
    if args.notices:
        INCLUDE_NOTICES = True # type: ignore
    _default_client = InvoiceValidatorClient(base_url=args.mustang_url)
    _default_kosit = KositValidatorClient(base_url=args.kosit_url)

    mustang_up = _default_client.health()
    kosit_up = _default_kosit.health()
    if not mustang_up:
        print(f"Mustang at {args.mustang_url} not reachable. "
              f"Start it with: docker compose up -d", file=sys.stderr)
    if not kosit_up:
        print(f"KoSIT at {args.kosit_url} not reachable. "
              f"Start it with: docker compose up -d", file=sys.stderr)
    if not (mustang_up or kosit_up):
        return 2

    targets = _iter_targets(args.paths)
    if not targets:
        print("no .pdf/.xml files found", file=sys.stderr)
        return 2

    bad = 0
    for path in targets:
        # Use a throwaway Report so findings get the same clean-invoice severity
        # policy the fuzzer applies, then read them back.
        report = _CliReport()
        try:
            outcome = _validate_path_into(path, report)
        except OSError as exc:
            print(f"SKIP  {path}: {exc}")
            continue
        verdict = ("VALID  " if outcome.valid else
                   "ERR    " if outcome.valid is None else "INVALID")
        print(f"{verdict} {path}  (errors={len(report.errors)} "
              f"warnings={len(report.warnings)})")
        if outcome.valid is not True:
            bad += 1
        shown = report.errors + (report.warnings if args.verbose else [])
        for line in shown:
            print(f"        {line}")
    print(f"\n{len(targets)} file(s), {bad} not-valid/unvalidatable")
    return 1 if bad else 0


@dataclass
class _CliReport:
    anomalies: list[str] = field(default_factory=list[str])
    errors: list[str] = field(default_factory=list[str])
    warnings: list[str] = field(default_factory=list[str])


def _validate_path_into(path: str, report: "_CliReport") -> ValidationOutcome:
    with open(path, "rb") as fh:
        raw = fh.read()
    if raw.startswith(b"%PDF"):
        return validate_zugferd(raw, report)
    return validate_xrechnung(raw, report)


