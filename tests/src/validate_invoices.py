class InvoiceValidatorClient:
    """Thin HTTP client for the dockerized Mustang validator."""

    def __init__(self, base_url: str = MUSTANG_URL,
                 timeout: int = HTTP_TIMEOUT,
                 session: requests.Session | None = None):
        self.base_url = base_url.rstrip("/")
        self.timeout = timeout
        self.session = session or requests.Session()

    def health(self) -> bool:
        try:
            r = self.session.get(f"{self.base_url}/health", timeout=5)
            return r.status_code == 200
        except requests.RequestException:
            return False

    def validate_bytes(self, raw: bytes, *, kind: str | None = None,
                        notices: bool = INCLUDE_NOTICES) -> ValidationOutcome:
        """Validate raw PDF or XML bytes. ``kind`` is auto-sniffed if omitted."""
        params: dict[str, str] = {"notices": "1" if notices else "0"}
        if kind:
            params["kind"] = kind
        try:
            r = self.session.post(
                f"{self.base_url}/validate",
                params=params,
                data=raw,
                headers={"Content-Type": "application/octet-stream"},
                timeout=self.timeout,
            )
        except requests.RequestException as exc:
            return ValidationOutcome(
                valid=None, transport_error=f"validator unreachable: {exc}")

        if r.status_code != 200:
            detail = ""
            try:
                detail = r.json().get("error", "")
            except ValueError:
                detail = r.text[:200]
            return ValidationOutcome(
                valid=None,
                transport_error=f"validator HTTP {r.status_code}: {detail}")

        payload: dict[str, Any] = r.json()
        outcome = parse_mustang_report(payload.get("report_xml", ""))
        # Prefer the parsed verdict; fall back to the server's boolean.
        if outcome.valid is None:
            outcome.valid = bool(payload.get("valid"))
        outcome.engine = payload.get("engine", "mustang")
        return outcome

    def extract_embedded_xml(self, pdf: bytes) -> bytes | None:
        """Return the embedded Factur-X/ZUGFeRD XML of a PDF, or None.

        None means either "no XML embedded" or the extractor was unreachable;
        callers treat a missing secondary input as non-fatal.
        """
        try:
            r = self.session.post(
                f"{self.base_url}/extract",
                data=pdf,
                headers={"Content-Type": "application/octet-stream"},
                timeout=self.timeout,
            )
        except requests.RequestException:
            return None
        if r.status_code != 200:
            return None
        payload = r.json()
        if not payload.get("found"):
            return None
        try:
            return base64.b64decode(payload["xml_b64"], validate=True)
        except Exception:
            return None


class KositValidatorClient:
    """HTTP client for the dockerized official KoSIT XRechnung validator.

    Talks to the KoSIT daemon: ``POST /`` with the XML body returns a VARL
    ``<rep:report>``; ``GET /server/health`` is the liveness probe.
    """

    def __init__(self, base_url: str = KOSIT_URL,
                 timeout: int = HTTP_TIMEOUT,
                 session: requests.Session | None = None):
        self.base_url = base_url.rstrip("/")
        self.timeout = timeout
        self.session = session or requests.Session()

    def health(self) -> bool:
        try:
            r = self.session.get(f"{self.base_url}/server/health", timeout=5)
            return r.status_code == 200
        except requests.RequestException:
            return False

    def validate_bytes(self, raw: bytes) -> ValidationOutcome:
        try:
            r = self.session.post(
                f"{self.base_url}/",
                data=raw,
                headers={"Content-Type": "application/xml"},
                timeout=self.timeout,
            )
        except requests.RequestException as exc:
            return ValidationOutcome(
                valid=None, engine="kosit",
                transport_error=f"KoSIT unreachable: {exc}")
        # The KoSIT daemon signals the verdict through the HTTP status (200 for
        # accept, 4xx such as 406 for reject) but always returns a VARL report
        # body. So a non-200 with a report is a *verdict*, not a transport error.
        body = r.text or ""
        if "varlVersion" in body:
            return parse_kosit_report(body)
        return ValidationOutcome(
            valid=None, engine="kosit",
            transport_error=f"KoSIT HTTP {r.status_code}: {body[:200]}")


# Process-wide default clients so the validate_* helpers are cheap to call.
_default_client: InvoiceValidatorClient | None = None
_default_kosit: KositValidatorClient | None = None
_client_lock = threading.Lock()
# Nag only once per engine about being down, not per invoice.
_unavailable_warned: set[str] = set()


def get_client() -> InvoiceValidatorClient:
    global _default_client
    with _client_lock:
        if _default_client is None:
            _default_client = InvoiceValidatorClient()
        return _default_client


def get_kosit_client() -> KositValidatorClient:
    global _default_kosit
    with _client_lock:
        if _default_kosit is None:
            _default_kosit = KositValidatorClient()
        return _default_kosit


# --------------------------------------------------------------------------- #
# Folding an outcome into the fuzzer's Report
# --------------------------------------------------------------------------- #

def _apply_outcome(outcome: ValidationOutcome, report: Any, *, doc: str,
                   engine: str = "validator", secondary: bool = False) -> None:
    """Merge a :class:`ValidationOutcome` into a fuzzer ``Report``.

    ``report`` is the ``generate_invoices.Report`` dataclass (duck-typed here to
    avoid a hard import cycle): it exposes ``.errors``, ``.warnings`` and
    ``.anomalies`` lists.

    Severity policy mirrors the fuzzer's own: a *clean* invoice (no recorded
    anomalies) that the validator rejects is a generator bug -> error. On an
    *edge* invoice we deliberately sent something odd, so a rejection is expected
    -> record it as a warning to keep the signal clean.

    ``secondary`` downgrades everything to warnings/notices: this is a supporting
    second opinion (KoSIT on the XML embedded in a ZUGFeRD PDF that Mustang has
    already validated), so it should add signal without escalating severity or
    double-counting what the primary engine already flagged.
    """
    if report is None:
        return

    # Transport / availability problems: not an invoice verdict.
    if outcome.valid is None:
        msg = f"{doc}: {engine} validation unavailable ({outcome.transport_error})"
        if VALIDATOR_REQUIRED and not secondary:
            report.errors.append(msg)
        elif engine not in _unavailable_warned:
            _unavailable_warned.add(engine)
            report.warnings.append(
                msg + " -- set INVOICE_VALIDATOR_REQUIRED=1 to treat as error, "
                "or start it with `docker compose up -d`")
        return

    is_edge = bool(getattr(report, "anomalies", None))

    def emit_error(line: str) -> None:
        if secondary or is_edge:
            report.warnings.append(line)
        else:
            report.errors.append(line)

    # A KoSIT "no scenario matched": the document is not a recognizable
    # XRechnung. As a primary XRechnung check that is a real failure; as a
    # secondary check on a plain ZUGFeRD CII it is expected -> just a notice.
    if outcome.scenario_matched is False:
        note = f"{doc}: [{outcome.engine}] no matching XRechnung scenario"
        if secondary:
            if INCLUDE_NOTICES:
                report.warnings.append(note + " (embedded XML is not an XRechnung)")
        else:
            emit_error(note)
        return

    for f in outcome.findings:
        line = f"{doc}: {f.one_line()}"
        if f.severity == "error":
            emit_error(line)
        elif f.severity == "warning":
            report.warnings.append(line)
        elif INCLUDE_NOTICES:
            report.warnings.append(line)

    # If the engine says "invalid" but produced no error-level messages (e.g. a
    # PDF/A container failure surfaced only in the summary), don't lose it.
    if outcome.valid is False and not outcome.errors:
        line = (f"{doc}: [{outcome.engine}] verdict INVALID "
                f"(sections={outcome.section_status or 'n/a'})")
        emit_error(line)


# --------------------------------------------------------------------------- #
# Public validation entry points
# --------------------------------------------------------------------------- #
# Call these from generate_invoices.py. They accept the value straight from the
# API response (a base64 string) or raw bytes, run the cheap structural checks
# first, then the deep HTTP validation, and fold any findings into ``report``
# (when given). Each also returns the ValidationOutcome for standalone use.

def _coerce_bytes(document: Any) -> tuple[bytes | None, str | None, str | None]:
    """Normalize a response value to raw bytes.

    Returns ``(raw, warning, error)``: exactly one of the three is meaningful.
    ``warning`` means "nothing to validate" (missing field); ``error`` means the
    value was present but unusable (bad base64 / wrong type).
    """
    if document is None or document == "":
        return None, "missing from response", None
    if isinstance(document, (bytes, bytearray)):
        return bytes(document), None, None
    if isinstance(document, str):
        try:
            return base64.b64decode(document, validate=True), None, None
        except Exception:
            return None, None, "is not valid base64"
    return None, None, f"has unexpected type {type(document).__name__}"


def _early(report: Any, *, doc: str, section: str,
           warning: str | None = None, error: str | None = None) -> ValidationOutcome:
    """Record a pre-check result and build the matching outcome.

    ``warning`` means "nothing to validate" (missing field) -> no verdict;
    ``error`` means the value was present but unusable -> invalid.
    """
    if warning is not None:
        msg = f"{doc}: {warning}"
        if report is not None:
            report.warnings.append(msg)
        return ValidationOutcome(
            valid=None,
            findings=[Finding("warning", msg, section=section)])
    msg = f"{doc}: {error}"
    if report is not None:
        report.errors.append(msg)
    return ValidationOutcome(
        valid=False,
        findings=[Finding("error", msg, section=section)])


def validate_zugferd(pdf: Any, report: Any = None, *,
                     client: InvoiceValidatorClient | None = None,
                     kosit: "KositValidatorClient | None" = None,
                     secondary_kosit: bool = True) -> ValidationOutcome:
    """Validate a ZUGFeRD / Factur-X document -- the WHOLE hybrid PDF.

    Primary: Mustang checks PDF/A-3 conformance *and* the embedded XML
    (XML-Schema + EN16931 / profile Schematron), i.e. the file, not just the data.

    Secondary (``secondary_kosit=True``): the embedded XML is extracted and also
    run through the official KoSIT XRechnung validator. Its findings are folded
    in as warnings (a supporting second opinion); if the embedded XML is a plain
    ZUGFeRD CII that matches no XRechnung scenario, that is treated as a notice,
    not a failure.

    ``pdf``    the ``invoicePdf`` value from the API response (base64 string) or
               raw PDF bytes.
    ``report`` optional fuzzer ``Report``; findings are folded in when given. The
               primary ``ValidationOutcome`` (Mustang) is always returned.
    """
    raw, warn, err = _coerce_bytes(pdf)
    if warn:
        return _early(report, doc="invoicePdf", section="pdf", warning=warn)
    if err:
        return _early(report, doc="invoicePdf", section="pdf", error=err)
    if raw is None:
        return _early(report, doc="invoicePdf", section="pdf",
                      error="Raw value is None")
    if not raw.startswith(b"%PDF"):
        return _early(report, doc="invoicePdf", section="pdf",
                      error="does not start with %PDF")

    mustang = client or get_client()
    outcome = mustang.validate_bytes(raw, kind="pdf")
    if report is not None:
        _apply_outcome(outcome, report, doc="invoicePdf", engine="mustang")

    # Secondary: KoSIT on the embedded XML.
    if secondary_kosit:
        embedded = mustang.extract_embedded_xml(raw)
        if embedded is not None:
            kosit_outcome = (kosit or get_kosit_client()).validate_bytes(embedded)
            if report is not None:
                _apply_outcome(kosit_outcome, report,
                               doc="invoicePdf[embedded-xml]",
                               engine="kosit", secondary=True)
    return outcome


def validate_xrechnung(xml: Any, report: Any = None, *,
                       kosit: "KositValidatorClient | None" = None) -> ValidationOutcome:
    """Validate an XRechnung / CII XML document with the official KoSIT validator.

    KoSIT is the German B2G reference: it checks the XML against XML-Schema, the
    EN16931 rules and the XRechnung CIUS (BR-DE) rules, and returns an accept /
    reject assessment. A document that matches no XRechnung scenario is reported
    as a failure (it is not a recognizable XRechnung).

    ``xml``    the ``invoiceXml`` value from the API response (base64 string) or
               raw XML bytes.
    ``report`` optional fuzzer ``Report`` (see :func:`validate_zugferd`).
    """
    raw, warn, err = _coerce_bytes(xml)
    if warn:
        return _early(report, doc="invoiceXml", section="xml", warning=warn)
    if err:
        return _early(report, doc="invoiceXml", section="xml", error=err)
    if raw is None:
        return _early(report, doc="invoiceXml", section="xml", error="Raw value is None")
    try:
        ElementTree.fromstring(raw)
    except ElementTree.ParseError as exc:
        return _early(report, doc="invoiceXml", section="xml",
                      error=f"is not well-formed XML: {exc}")
    outcome = (kosit or get_kosit_client()).validate_bytes(raw)
    if report is not None:
        _apply_outcome(outcome, report, doc="invoiceXml", engine="kosit")
    return outcome


