HTTP_TIMEOUT = int(os.environ.get("INVOICE_VALIDATOR_TIMEOUT", "150"))


# --------------------------------------------------------------------------- #
# Result model
# --------------------------------------------------------------------------- #

@dataclass
class Finding:
    """One rule result from the validator (an error, warning or notice)."""
    severity: str          # "error" | "warning" | "notice"
    message: str
    code: str = ""         # e.g. "BR-16", "18", "23" (rule / Mustang error type)
    section: str = ""      # "pdf" | "xml" | ""
    location: str = ""     # XPath, when the engine reports one

    def one_line(self) -> str:
        where = f"[{self.section}]" if self.section else ""
        code = f" {self.code}" if self.code else ""
        return f"{where}{code} {self.message}".strip()


@dataclass
class ValidationOutcome:
    """Structured result of validating a single document."""
    # True/False = a real verdict from the engine; None = we could not get one
    # (validator down, transport error, etc.) and the caller should degrade.
    valid: bool | None
    engine: str = "mustang"
    section_status: dict[str, str] = field(default_factory=dict[str, str])
    findings: list[Finding] = field(default_factory=list[Finding])
    report_xml: str = ""
    transport_error: str | None = None   # set when valid is None
    # KoSIT only: False when the document matched no scenario (e.g. a plain
    # ZUGFeRD CII that is not an XRechnung); None for the Mustang engine.
    scenario_matched: bool | None = None

    @property
    def errors(self) -> list[Finding]:
        return [f for f in self.findings if f.severity == "error"]

    @property
    def warnings(self) -> list[Finding]:
        return [f for f in self.findings if f.severity == "warning"]


# --------------------------------------------------------------------------- #
# Report parsing (Mustang <validation> XML)
# --------------------------------------------------------------------------- #

# Mustang error "type" codes that concern the PDF container rather than a
# business rule -- useful for a friendlier label, purely cosmetic.
_MUSTANG_PDF_ERROR_TYPES = {"23", "24", "25", "26", "27"}


def _extract_code(text: str, elem_type: str) -> str:
    """Pull a rule id like ``BR-16`` / ``PEPPOL-EN16931-R008`` from the message,
    falling back to Mustang's numeric ``type`` attribute."""
    t = text.strip()
    if t.startswith("["):
        end = t.find("]")
        if end != -1:
            return t[1:end]
    return elem_type or ""


def _clean_message(text: str, code: str) -> str:
    """Tidy a raw rule message: drop the leading ``[CODE]-`` (we surface the code
    separately) and the trailing ``from /xslt/....xslt)`` engine breadcrumb."""
    t = " ".join(text.split())  # collapse whitespace/newlines
    if code and t.startswith(f"[{code}]"):
        t = t[len(code) + 2:].lstrip("-").strip()
    lower = t.lower()
    marker = " from /xslt/"
    idx = lower.find(marker)
    if idx != -1:
        t = t[:idx].rstrip()
        if t.endswith(("(", "from")):
            t = t.rstrip("(from ").rstrip()
    return t


def parse_mustang_report(report_xml: str) -> ValidationOutcome:
    """Parse a Mustang ``<validation>`` report into a :class:`ValidationOutcome`.

    The report has an optional ``<pdf>`` section (veraPDF PDF/A result) and/or an
    ``<xml>`` section (schema + Schematron), each carrying a ``<messages>`` block
    of ``<error>`` / ``<warning>`` / ``<notice>`` elements and a per-section
    ``<summary status="valid|invalid"/>``. A top-level ``<summary>`` gives the
    overall verdict.
    """
    try:
        root = ElementTree.fromstring(report_xml)
    except ElementTree.ParseError as exc:
        return ValidationOutcome(
            valid=None, report_xml=report_xml,
            transport_error=f"could not parse validator report: {exc}")

    findings: list[Finding] = []
    section_status: dict[str, str] = {}

    for section_name in ("pdf", "xml"):
        section = root.find(section_name)
        if section is None:
            continue
        summ = section.find("summary")
        if summ is not None and summ.get("status"):
            section_status[section_name] = summ.get("status", "")
        messages = section.find("messages")
        if messages is None:
            continue
        for elem in messages:
            severity = elem.tag.lower()      # error | warning | notice
            if severity not in ("error", "warning", "notice"):
                continue
            text = (elem.text or "").strip()
            code = _extract_code(text, elem.get("type", ""))
            findings.append(Finding(
                severity=severity,
                message=_clean_message(text, code),
                code=code,
                section=section_name,
                location=elem.get("location", ""),
            ))

    # Overall verdict: the last top-level <summary> (Mustang emits section
    # summaries first, then the overall one).
    overall = None
    for summ in root.findall("summary"):
        status = summ.get("status")
        if status:
            overall = status.lower() == "valid"
    if overall is None and section_status:
        overall = all(v.lower() == "valid" for v in section_status.values())

    return ValidationOutcome(
        valid=overall,
        section_status=section_status,
        findings=findings,
        report_xml=report_xml,
    )


# --------------------------------------------------------------------------- #
# Report parsing (KoSIT VARL <rep:report> XML)
# --------------------------------------------------------------------------- #
# The KoSIT validator returns a VARL report:
#   <rep:report valid="true|false">
#     <engine><name>KoSIT Validator 1.6.2</name></engine>
#     <scenarioMatched> | <noScenarioMatched>
#        <validationStepResult id=... valid=...>
#           <message level="error|warning|information" code="BR-DE-1" xpathLocation=...>[BR-DE-1]-...</message>
#     <assessment><accept/> | <reject/></assessment>
# Namespaces vary, so we match by local tag name throughout.

_KOSIT_LEVEL = {"error": "error", "warning": "warning",
                "information": "notice", "info": "notice"}


def _local(elem: Any) -> str:
    return elem.tag.split("}")[-1]


def parse_kosit_report(report_xml: str) -> ValidationOutcome:
    """Parse a KoSIT VARL ``<rep:report>`` into a :class:`ValidationOutcome`."""
    try:
        root = ElementTree.fromstring(report_xml)
    except ElementTree.ParseError as exc:
        return ValidationOutcome(
            valid=None, engine="kosit", report_xml=report_xml,
            transport_error=f"could not parse KoSIT report: {exc}")

    valid = (root.get("valid") or "").strip().lower() == "true"

    scenario_matched: bool | None = None
    engine = "kosit"
    findings: list[Finding] = []
    section_status: dict[str, str] = {}

    for child in root:
        name = _local(child)
        if name == "engine":
            for e in child:
                if _local(e) == "name" and e.text:
                    engine = e.text.strip()
        elif name == "noScenarioMatched":
            scenario_matched = False
        elif name == "scenarioMatched":
            scenario_matched = True
            for vsr in child:
                if _local(vsr) != "validationStepResult":
                    continue
                step = vsr.get("id", "")
                if vsr.get("valid"):
                    section_status[step] = vsr.get("valid", "")
                for msg in vsr.iter():
                    if _local(msg) != "message":
                        continue
                    level = (msg.get("level") or "error").lower()
                    severity = _KOSIT_LEVEL.get(level, "error")
                    text = (msg.text or "").strip()
                    code = msg.get("code", "") or _extract_code(text, "")
                    findings.append(Finding(
                        severity=severity,
                        message=_clean_message(text, code),
                        code=code,
                        section="xml",
                        location=msg.get("xpathLocation", ""),
                    ))
        elif name == "assessment":
            for a in child:
                section_status["assessment"] = _local(a)  # accept | reject

    return ValidationOutcome(
        valid=valid,
        engine=engine,
        section_status=section_status,
        findings=findings,
        report_xml=report_xml,
        scenario_matched=scenario_matched,
    )


